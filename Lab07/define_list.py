# تعريف list[]
# list تقبل انواع مختلفة من البانات بداخلها
letter = ["a" , "b" , "c" , "d" , "e"]
#  index =  0 ,  1  ,  2  ,  3  ,  4
#    or
#  index = -5 , -4  , -3  , -2  , -1 
print(type(letter))
# طباعة list
print(letter)
# العناوين (index) في list تبدأ من 0
# طباعة العنصر في الموقع الثاني ( index = 1 ) 
print(letter[1])  # ---> b
print(letter[-4]) # ---> b