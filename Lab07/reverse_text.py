# النص عبارة عن list من الاحرف
text = "python"
# مثال على ان النص list من الاحرف
print(text[1]) # y

# مثال على عكس النص
# يبدأ من العنوان -1 ( من الاخير )
# يتوقف عند -7 ( وليس -6 كي يطبع اخر حرف )
# الخطوة -1 ( مثال -1-1 = -2 الحرف الثاني من الاخير )
print(text[-1:-7:-1])

# طريقة اخرى
# طالما ان الخطوة تساوي -1 ، يتم طباعة النص من الاخير الى الاول
print(text[::-1])